"""
基本数据类型
1.整型
2.浮点型
3.布尔型
"""
# int   整型
num01 : int = 100
print(type(num01))
print(id(num01))

# float 浮点型
num02 : float = 100.1
print(type(num02))
print(id(num02))

# bool  布尔型
b01 : bool = True
b03 : bool = True
b02 : bool = False
print(type(b01))
print(id(b01))
print(type(b02))
print(id(b02))
print(type(b03))
print(id(b03))
